public class DuplicateNameException extends Exception {

	private static final long serialVersionUID = 1L;

	public DuplicateNameException() {
		super("A duplicate name has been used where it is not allowed.");
	}

	public DuplicateNameException(String p0) {
		super(p0);
	}

	public DuplicateNameException(Throwable p0) {
		super(p0);
	}

	public DuplicateNameException(String p0, Throwable p1) {
		super(p0, p1);
	}
}